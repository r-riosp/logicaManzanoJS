alert("Volume de uma lata de óleo")
const PI = 3.1415
let raio = prompt("Digite o raio da lata de óleo: ")
let altura = prompt("Digite a altura da lata de óleo: ")
let volume = PI * raio ** 2 * altura
alert(`O volume da lata de óleo é: ${volume.toFixed(2)} cm³`)

//exemplo de valores
// raio = 5 cm
// altura = 10 cm
// volume = 3.1415 * 5 ** 2 * 10 = 785.375 cm³
// raio = 10 cm
// altura = 20 cm
// volume = 3.1415 * 10 ** 2 * 20 = 6283.5 cm³